package Surat;

public class Surat02 {
    public String idSurat, namaMahasiswa, kelas;
    public char jenisSurat;
    public int durasi;

    public Surat02(){}

    public Surat02(String idSurat, String namaMahasiswa, String kelas, char jenisSurat, int durasi){
        this.idSurat = idSurat;
        this.namaMahasiswa = namaMahasiswa;
        this.kelas = kelas;
        this.jenisSurat = jenisSurat;
        this.durasi = durasi;
    }
}
